public class operator {
    public static void main(String[] args) {
        double a = 50.5, b = 10.1;
        System.out.println(a + " + "+b+" = "+(a + b));
        System.out.println(a + " - "+b+" = "+(a - b));
        System.out.println(a + " * "+b+" = "+(a * b));
        System.out.println(a + " / "+b+" = "+(int)(a / b));
        System.out.println(a + " % "+b+" = "+(a % b));
    }
}
